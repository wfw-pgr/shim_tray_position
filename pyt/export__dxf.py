import numpy as np
import os, sys
import ezdxf


# ========================================================= #
# ===  export__dxf.py                                   === #
# ========================================================= #

def export__dxf():

    # ------------------------------------------------- #
    # --- [1]                         --- #
    # ------------------------------------------------- #
    doc = ezdxf.new('R2010')
    msp = doc.modelspace() 
    msp.add_circle( center=[ 0, 0], radius=0.01 )
    doc.saveas('msh/out.dxf')
    return()


# ========================================================= #
# ===   Execution of Pragram                            === #
# ========================================================= #

if ( __name__=="__main__" ):
    export__dxf()

